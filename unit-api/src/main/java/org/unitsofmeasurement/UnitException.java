package org.unitsofmeasurement;

/**
 * Provides support for general failures of this package.
 * 
 * @author Steven R. Emmerson
 * @author Werner Keil
 * @version $Id: UnitException.java 64 2006-07-12 22:30:50Z edavis $
 */
public abstract class UnitException extends Exception {

    /** The serialVersionUID */
    private static final long serialVersionUID = 3333261489669700628L;

    /**
     * Constructs from nothing.
     */
    public UnitException() {
    }

    /**
     * Constructs from an error message.
     * 
     * @param message
     *            The error message.
     */
    public UnitException(final String message) {
	super(message);
    }

    /**
     * Constructs from a message and the exception that caused the failure.
     * 
     * @param message
     *            The message.
     * @param e
     *            The exception that caused the failure.
     */
    public UnitException(final String message, final Throwable e) {
	super(message);
	initCause(e);
    }

    /**
     * Constructs a new exception with the specified cause and a detail message
     * of <tt>(cause==null ? null : cause.toString())</tt> (which typically
     * contains the class and detail message of <tt>cause</tt>). This
     * constructor is useful for exceptions that are little more than wrappers
     * for other throwables (for example,
     * {@link java.security.PrivilegedActionException}).
     * 
     * @param cause
     *            the cause (which is saved for later retrieval by the
     *            {@link #getCause()} method). (A <tt>null</tt> value is
     *            permitted, and indicates that the cause is nonexistent or
     *            unknown.)
     */
    public UnitException(Throwable causingException) {
	super(causingException);
    }
}
